namespace Endroid\Flusher\Bundle\FlusherBundle;

use Endroid\Flusher\Bundle\FlusherBundle\DependencyInjection\Compiler\ReplaceEntityManagerPass;
use Symfony\Component\DependencyInjection\Compiler\PassConfig;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\HttpKernel\Bundle\Bundle;

class EndroidFlusherBundle extends Bundle
{
    /**
     * {@inheritdoc}
     */
    public function build(ContainerBuilder $container)
    {
        parent::build($container);

        $container->addCompilerPass(new ReplaceEntityManagerPass());
    }
}
